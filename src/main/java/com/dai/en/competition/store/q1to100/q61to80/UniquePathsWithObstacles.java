	public int uniquePathsWithObstacles(int[][] obstacleGrid) {
		int m = obstacleGrid.length;
		int n = obstacleGrid[0].length;
		if (obstacleGrid[0][0] == 1 || obstacleGrid[m - 1][n - 1] == 1) {
			return 0;
		}

		Map<Point, Integer> map = new HashMap<Point, Integer>();
		Point start = new Point(0, 0);
		map.put(start, 1);

		boolean over = false;

		while (true) {

			Set<Point> set = map.keySet();
			Map<Point, Integer> nMap = new HashMap<Point, Integer>();
			for (Point p : set) {

				if (p.x == m - 1 && p.y == n - 1) {
					over = true;
					break;
				}

				if (p.x < m - 1) {
					if (obstacleGrid[p.x + 1][p.y] != 1) {
						Point point = new Point(p.x + 1, p.y);
						if (nMap.containsKey(point)) {
							nMap.put(point, nMap.get(point) + map.get(p));
						} else {
							nMap.put(point, map.get(p));
						}
					}
				}
				if (p.y < n - 1) {
					if (obstacleGrid[p.x][p.y + 1] != 1) {
						Point point = new Point(p.x, p.y + 1);
						if (nMap.containsKey(point)) {
							nMap.put(point, nMap.get(point) + map.get(p));
						} else {
							nMap.put(point, map.get(p));
						}
					}
				}
			}

			if (!over) {
				if (nMap.size() == 0) {
					return 0;
				}
				map.clear();
				map.putAll(nMap);
			} else {
				break;
			}
		}

		Set<Point> set = map.keySet();
		int sum = 0;
		for (Point p : set) {
			sum = sum + map.get(p);
		}

		return sum;
	}

